#include<bits/stdc++.h>
using namespace std;

int main(){

    map<string,int>id; //string- index, int- value.

    id["omaer"]=2;// index id["omaer"] value is 2.
    id["alam"]=5;// index id["alam"] value is 5.
    id["md"]=7;// index id["md"] value is 7.

    cout<<id["alam"]<<endl;

return 0;
}

